					<p>Attack</p>
					<p><i>A stat that indicates how much damage a character will do when it attacks.</i></p>
					<p>Battlecry</p>
					<p><i>A minion effect that activates when the minion is played.</i></p>
					<p>Board Clear</p>
					<p><i>A move or combination of moves that removes all of the enemy minions from the board.</i></p>
					<p>Board Control</p>
					<p><i>The situation where one player has control over the board.</i></p>
					<p>Card Advantage</p>
					<p><i>The situation where one player has more cards in hand and/or in play.</i></p>
					<p>Character</p>
					<p><i>A minion or hero.</i></p>
					<p>Charge</p>
					<p><i>A minion effect that allows the minion to attack on the same turn that it is played.</i></p>
					<p>Deathrattle</p>
					<p><i>A minion effect that activates when the minion dies.</i></p>
					<p>Divine Shield</p>
					<p><i>A minion effect that stops the minion from taking damage the first time it is hit.</i></p>
					<p>Enrage</p>
					<p><i>A minion effect that activates when the minion is wounded.</i></p>
					<p>Freeze</p>
					<p><i>An effect that prevents a character from attacking for one turn.</i></p>
					<p>Health</p>
					<p><i>A stat that indicates how much damage a character can take before dying.</i></p>
					<p>Immune</p>
					<p><i>An effect that prevents a character from taking any damage.</i></p>
					<p>Mana</p>
					<p><i>A resource that is used to play cards.</i></p>
					<p>Mana Curve</p>
					<p><i>A graph indicating the relative mana costs of cards in a deck</i></p>
					<p>Mulligan</p>
					<p><i>The stage at the start of a match where you can choose to replace any number of your starting cards.</i></p>
					<p>Over-extend</p>
					<p><i>The situation where a player puts more minions onto the board than they need to.  This can leave a player vulnerable to a Board Clear.</i></p>
					<p>Play</p>
					<p><i>A move or set of moves.</i></p>
					<p>RNG</p>
					<p><i>Stands for Random Number Generation.  Refers to a situation where an outcome is randomly determined.</i></p>
					<p>Silence</p>
					<p><i>An effect that removes all other effects on a minion.</i></p>
					<p>Spell Damage</p>
					<p><i>A minion effect that increases the amount of damage that the spells cast by the owner deal.</i></p>
					<p>Stealth</p>
					<p><i>A minion effect that prevents the minion from being targeted.  Stealth is broken when the minion attacks or deals damage.</i></p>
					<p>Synergy</p>
					<p><i>Refers to the situation where two or more cards work together to become stronger than they could be individually.</i></p>
					<p>Taunt</p>
					<p><i>A minion effect that prevents opposing minions from attacking other characters.</i></p>
					<p>Trading</p>
					<p><i>The situation where a minion attacks another minion and both minions die.</i></p>
					<p>Windfury</p>
					<p><i>A minion effect that allows the minion to attack twice each turn.</i></p>
