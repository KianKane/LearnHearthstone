					<p>Building decks in arena mode is very different from building decks normally.  You don’t know what options you will have.  Here are some guidelines to follow when drafting decks for arena.</p>
					<h2>Picking a class</h2>
					<p>Not all classes are equal in arena.  Due to the nature of how decks are build some classes come out on top with more relevant cards.  Great picks are Mage, Rogue and Paladin.  Decent picks are Druid, Shaman and Hunter.  Poor picks are Warlock Priest and Warrior.</p>
					<h2>Drafting the first ten cards</h2>
					<div class="guide_image">
						<img src="images/arena.png" alt="Arena key"></img>
						<p>An arena key</p>
					</div> <!-- /guide_image -->
					<p>Once you have picked your class it’s time to jump in and start picking cards.  Start off by grabbing the strongest of the available cards.  Don’t aim for synergy cards or situational cards.  Just pick the most solid options.  AOE spells and high health minions are great choices here.  I recommend looking up an online tier list for the first ten or so cards.</p>
					<h2>Drafting the rest of the deck</h2>
					<p>After you have picked the first ten or so cards a tier list will no longer cut it.  You have enough cards now that the rest should be picked based on what your deck does and does not contain.  Try to make sure you have a relatively smooth mana curve and pick cards that work well with your previous choices.</p>
