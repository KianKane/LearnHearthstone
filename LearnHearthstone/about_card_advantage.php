					<p>Sometimes it can be difficult to tell who is winning a match in Hearthstone.  The person with the most health is not always ahead.  To really work out who is ahead its best to look at a number of things, one of which is card advantage.</p>
					<p>A player is considered to have card advantage when he has more cards than the opponent.  This includes cards in the hand and in play.  The more cards someone has the more power and options they have.</p>
					<p>There is two main methods of generating card advantage.</p>
					<h2>Method 1 – Drawing cards</h2>
					<p>The more cards you draw the more cards you have in play rather than sitting in your deck.  Almost all decks will want some card draw.</p>
					<h2>Method 2 – Getting value</h2>
					<div class="guide_image">
						<img src="images/flamestrike.png" alt="Flamestrike"></img>
						<p>The card art for Flamestrike</p>
					</div> <!-- /guide_image -->
					<p>The other method is to get more value out of your cards.  Rather than getting more cards yourself this involves getting rid of your opponent’s cards.  An example of this could be using Flamestrike (A spell that damages all enemy minions) to kill three opposing minions, this would mean that you would have expended a single card while removing three of the opponents.</p>
					<p>It’s important to keep both card draw and value in mind when both creating decks and playing matches.  A healthy mix of both will almost always help you pull ahead and win the match.</p>
